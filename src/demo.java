//Type casting

class Demo
{
    public static void main(String args[])
    {   int i=50;
        double d=i;//this is implicit type casting
        System.out.println(i);
        System.out.println(d);


        double myval=45.85;
        int myint=(int)myval;//thids is explicit type casting
        System.out.println(myval);
        System.out.println(myint);
    }
}

